// when defining a function, in the parenthesis following the function name, you put in parameters. These are variables local to the function and when called, you put values in the parenthesis of the call to be used as those parameters.

// Example
function ourFunctionWithArgs(a, b) {
  console.log(a - b);
}
ourFunctionWithArgs(10, 5); // Outputs 5

// Only change code below this line.
